GWO_FS_WOLVES = 30
GWO_FS_ITER   = 80
GWO_FS_ALPHA  = 0.9
GWO_FS_CV     = 5
GWO_FS_THRESH = 0.5

def _make_eval_model():
  return xgb.XGBClassifier(
      n_estimators=100, max_depth=3, learning_rate=0.1,
      subsample=0.8, colsample_bytree=0.8, min_child_weight=3,
      eval_metric='logloss', random_state=SEED, tree_method='hist'
      )

def fitness_fs(mask, X, y):
  sel = np.where(mask > GWO_FS_THRESH)[0]
  if len(sel) == 0: return 1.0
  pipe = Pipeline([('sc', RobustScaler()), ('clf', _make_eval_model())])
  cv   = StratifiedKFold(n_splits=GWO_FS_CV, shuffle=True, random_state=SEED)
  acc  = cross_val_score(pipe, X[:, sel], y, cv=cv, scoring='accuracy', n_jobs=-1).mean()
  return (1 - GWO_FS_ALPHA) * (len(sel)/X.shape[1]) + GWO_FS_ALPHA * (1 - acc)

def gwo_feature_selection(X, y, n_wolves=GWO_FS_WOLVES, max_iter=GWO_FS_ITER):
  n_feat = X.shape[1]
  wolves = np.random.rand(n_wolves, n_feat)  # Initialize positions randomly [0,1]
  apos, ascore = np.zeros(n_feat), float('inf')  # Best wolf (α)
  bpos, bscore = np.zeros(n_feat), float('inf')  # 2nd best wolf (β)
  dpos, dscore = np.zeros(n_feat), float('inf')  # 3rd best wolf (δ)
  history = []

  for t in range(max_iter):
    for i in range(n_wolves):
      s = fitness_fs(wolves[i], X, y)
      # Update three best wolves
      if s < ascore:
        dscore, dpos = bscore, bpos.copy()
        bscore, bpos = ascore, apos.copy()
        ascore, apos = s, wolves[i].copy()

      elif s < bscore:
        dscore, dpos = bscore, bpos.copy()
        bscore, bpos = s, wolves[i].copy()

      elif s < dscore:
        dscore, dpos = s, wolves[i].copy()

    history.append(ascore)
    a = 2.0 - t * (2.0 / max_iter)  # Linearly decrease a from 2 to 0

    # Update wolf positions using α, β, δ guidance
    for i in range(n_wolves):
      new = np.zeros(n_feat)
      for j in range(n_feat):
        r1, r2 = random.random(), random.random()
        X1 = apos[j] - (2*a*r1 - a) * abs(2*r2*apos[j] - wolves[i][j])
        r1, r2 = random.random(), random.random()
        X2 = bpos[j] - (2*a*r1 - a) * abs(2*r2*bpos[j] - wolves[i][j])
        r1, r2 = random.random(), random.random()
        X3 = dpos[j] - (2*a*r1 - a) * abs(2*r2*dpos[j] - wolves[i][j])
        new[j] = (X1 + X2 + X3) / 3
      wolves[i] = np.clip(new, 0, 1)

    print(f'  GWO-FS {t+1:3d}/{max_iter} | cost={ascore:.4f} | feat={(apos>GWO_FS_THRESH).sum()}')
  return np.where(apos > GWO_FS_THRESH)[0], history

print(f'GWO Feature Selection | wolves={GWO_FS_WOLVES} iters={GWO_FS_ITER} cv=StratifiedKFold({GWO_FS_CV})')
