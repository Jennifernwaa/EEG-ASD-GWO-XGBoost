GWO_HP_WOLVES = 30
GWO_HP_ITER   = 100
GWO_HP_CV     = 5

HP_BOUNDS = np.array([
    [0.01, 0.3],   # learning_rate
    [2,    4   ],   # max_depth
    [50,   200 ],   # n_estimators
    [0.5,  1.0 ],   # subsample
    [0.5,  1.0 ],   # colsample_bytree
    [3,    10  ],   # min_child_weight
    [0,    5   ],   # gamma
    [1, 8], #reg alpha
    [1, 10] # reg lambda

])

def decode_hp(pos):
  return {
      'learning_rate':    float(np.clip(pos[0], *HP_BOUNDS[0])),
      'max_depth':        int(round(np.clip(pos[1], *HP_BOUNDS[1]))),
      'n_estimators':     int(round(np.clip(pos[2], *HP_BOUNDS[2]))),
      'subsample':        float(np.clip(pos[3], *HP_BOUNDS[3])),
      'colsample_bytree': float(np.clip(pos[4], *HP_BOUNDS[4])),
      'min_child_weight': int(round(np.clip(pos[5], *HP_BOUNDS[5]))),
      'gamma':            float(np.clip(pos[6], *HP_BOUNDS[6])),
      'reg_alpha':        float(np.clip(pos[7], *HP_BOUNDS[7])),
      'reg_lambda':       float(np.clip(pos[8], *HP_BOUNDS[8])),
      }

def fitness_hp(pos, X, y):
  hp = decode_hp(pos)
  obj = 'binary:logistic' if n_classes == 2 else 'multi:softmax'
  clf = xgb.XGBClassifier(**hp, objective=obj, eval_metric='logloss',
                            tree_method='hist', random_state=SEED)
  pipe = Pipeline([('sc', RobustScaler()), ('clf', clf)])
  cv = StratifiedKFold(n_splits=GWO_HP_CV, shuffle=True, random_state=SEED)
  return 1.0 - cross_val_score(pipe, X, y, cv=cv, scoring='accuracy', n_jobs=-1).mean()

def gwo_hyperparameter_tuning(X, y, bounds=HP_BOUNDS, n_wolves=GWO_HP_WOLVES, max_iter=GWO_HP_ITER):
  dim = bounds.shape[0]
  wolves = bounds[:, 0] + np.random.rand(n_wolves, dim) * (bounds[:, 1] - bounds[:, 0])
  apos, ascore = np.zeros(dim), float('inf')
  bpos, bscore = np.zeros(dim), float('inf')
  dpos, dscore = np.zeros(dim), float('inf')
  history = []

  for t in range(max_iter):
      for i in range(n_wolves):
          s = fitness_hp(wolves[i], X, y)
          if s < ascore:
              dscore, dpos = bscore, bpos.copy()
              bscore, bpos = ascore, apos.copy()
              ascore, apos = s, wolves[i].copy()
          elif s < bscore:
              dscore, dpos = bscore, bpos.copy()
              bscore, bpos = s, wolves[i].copy()
          elif s < dscore:
              dscore, dpos = s, wolves[i].copy()

      history.append(ascore)
      a = 2.0 - t * (2.0 / max_iter)

      for i in range(n_wolves):
          new = np.zeros(dim)
          for j in range(dim):
              r1, r2 = random.random(), random.random()
              X1 = apos[j] - (2*a*r1 - a) * abs(2*r2*apos[j] - wolves[i][j])
              r1, r2 = random.random(), random.random()
              X2 = bpos[j] - (2*a*r1 - a) * abs(2*r2*bpos[j] - wolves[i][j])
              r1, r2 = random.random(), random.random()
              X3 = dpos[j] - (2*a*r1 - a) * abs(2*r2*dpos[j] - wolves[i][j])
              new[j] = (X1 + X2 + X3) / 3
          wolves[i] = np.clip(new, bounds[:, 0], bounds[:, 1])

      print(f'  GWO-HP {t+1:3d}/{max_iter} | best CV acc={(1-ascore)*100:.2f}%')
  return decode_hp(apos), ascore, history

print(f'GWO Hyperparameter Tuning | wolves={GWO_HP_WOLVES} iters={GWO_HP_ITER} cv=StratifiedKFold({GWO_HP_CV})')
